#include<stdio.h>
//                  Increment and Decrement Operator(unary operators)
int main()
{
    int i=1; //     i+=1, i++, i=i+1

    //++i prefix or pre increment
    //i++ postfix or post increment

    printf("%d\n",i);
    printf("%d\n",i++); // use then increase
    printf("%d\n",i);
    printf("%d\n",++i); // increase then use
    printf("%d\n",i);
    
   
}